  END IF;

  IF to_regclass('public.replyfeedback') IS NOT NULL THEN
    CREATE SEQUENCE IF NOT EXISTS public.replyfeedback_rep_feedback_id_seq;
    SELECT COALESCE(MAX(rep_feedback_id), 0) INTO current_max FROM public.replyfeedback;
