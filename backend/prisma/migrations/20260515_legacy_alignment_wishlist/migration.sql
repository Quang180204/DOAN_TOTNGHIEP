ALTER TABLE IF EXISTS public.accountaddress
  ALTER COLUMN "isDefault" TYPE boolean
  USING ("isDefault" = B'1');

DO $$
DECLARE
  current_max bigint;
BEGIN
  IF to_regclass('public.accountaddress') IS NOT NULL THEN
    CREATE SEQUENCE IF NOT EXISTS public.accountaddress_account_address_id_seq;
    SELECT COALESCE(MAX(account_address_id), 0) INTO current_max FROM public.accountaddress;
    PERFORM setval('public.accountaddress_account_address_id_seq', GREATEST(current_max, 1), current_max > 0);
    ALTER TABLE public.accountaddress ALTER COLUMN account_address_id SET DEFAULT nextval('public.accountaddress_account_address_id_seq');
  END IF;

  IF to_regclass('public.contact') IS NOT NULL THEN
    CREATE SEQUENCE IF NOT EXISTS public.contact_contact_id_seq;
    SELECT COALESCE(MAX(contact_id), 0) INTO current_max FROM public.contact;
