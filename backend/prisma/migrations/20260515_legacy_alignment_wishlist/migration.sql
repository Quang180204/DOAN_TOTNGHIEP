    ALTER TABLE public.discount ALTER COLUMN disscount_id SET DEFAULT nextval('public.discount_disscount_id_seq');
  END IF;

  IF to_regclass('public.feedback') IS NOT NULL THEN
    CREATE SEQUENCE IF NOT EXISTS public.feedback_feedback_id_seq;
    SELECT COALESCE(MAX(feedback_id), 0) INTO current_max FROM public.feedback;
    PERFORM setval('public.feedback_feedback_id_seq', GREATEST(current_max, 1), current_max > 0);
    ALTER TABLE public.feedback ALTER COLUMN feedback_id SET DEFAULT nextval('public.feedback_feedback_id_seq');
  END IF;

  IF to_regclass('public.orderaddress') IS NOT NULL THEN
    CREATE SEQUENCE IF NOT EXISTS public.orderaddress_orderAddressId_seq;
    SELECT COALESCE(MAX("orderAddressId"), 0) INTO current_max FROM public.orderaddress;
    PERFORM setval('public.orderaddress_orderAddressId_seq', GREATEST(current_max, 1), current_max > 0);
    ALTER TABLE public.orderaddress ALTER COLUMN "orderAddressId" SET DEFAULT nextval('public.orderaddress_orderAddressId_seq');
  END IF;

  IF to_regclass('public.payment') IS NOT NULL THEN
    CREATE SEQUENCE IF NOT EXISTS public.payment_payment_id_seq;
